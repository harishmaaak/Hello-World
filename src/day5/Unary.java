package day5;

public class Unary {
    public static void main(String[] args){
        int i =10;
        boolean a = false;
        System.out.println(+i);
        System.out.println(-i);
        System.out.println(++i);
        System.out.println(i++);
        System.out.println(--i);
        System.out.println(i--);
        System.out.println(!a);
    }
}
